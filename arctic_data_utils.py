def rotate_mat(axis,radian):
    rot_matrix = linalg.expm(np.cross(np.eye(3),axis/linalg.norm(axis) * radian))
    return rot_matrix

def get_Ro(x_angle, y_angle, z_angle):
    x_axis = [1,0,0]
    x_r = (2 * math.pi) * (x_angle / 360)
    x_world = rotate_mat(x_axis, x_r)
    
    y_axis = [0,1,0]
    y_r = (2 * math.pi) * (y_angle / 360)
    y_world = rotate_mat(y_axis, y_r)
    
    z_axis = [0,0,1]
    z_r = (2 * math.pi) * (z_angle / 360)
    z_world = rotate_mat(z_axis, z_r)
    R_world = z_world @ y_world @ x_world
    return R_world


